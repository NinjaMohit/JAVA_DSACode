class SwichDemo {
	public static void main(String[] args) {
		int x = 3;
		switch (x) {
			case 1:
				System.out.println("One");
				break;
			case 2:
				System.out.println("two");
				break;
			case 3:
				System.out.println("three");
				break;
			case 4:
				System.out.println("four");
				break;
			default:
				System.out.println("NoMAtch");
				break;

		}
		System.out.println("After Switch");
	}
}
