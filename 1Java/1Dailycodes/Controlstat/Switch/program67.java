class Program67 {
     public static void main(String[] args) {

         int ch = 65;
         switch (ch) {
             default:
                 System.out.println("No match");
             case 'A':               // Ascii value of A=65
                 System.out.println("char-A");
                 break;
             case 67:
                 System.out.println("Num-65");
                 break;
             case 'B':
                 System.out.println("char-B");
                 break;
             case 68:
                 System.out.println("Num-66");
                 break;


         }
     }
 }
